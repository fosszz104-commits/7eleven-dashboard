export function fB(n) {
  if (n >= 1e8) return `${(n/1e8).toFixed(2)}B`
  if (n >= 1e6) return `${(n/1e6).toFixed(2)}M`
  if (n >= 1e3) return `${(n/1e3).toFixed(1)}K`
  return n.toLocaleString('th')
}
export function fBF(n) { return n.toLocaleString('th', { maximumFractionDigits: 0 }) + ' ฿' }
export function pct(a, b) { return b ? ((a - b) / b * 100) : 0 }
